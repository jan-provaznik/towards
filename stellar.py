# A broad variety of stellar rank witnesses can be constructed using
# the algorithm introduced in (Fiurasek, 2022).
#
# The concept of genuine quantum non-Gaussian hierarchies, introduced
# in (Lachman, 2019), employs a particular variant of a stellar rank
# witness that relies on the diagonal elements of the examined state.
#
# The witness from (Lachman, 2019) can be restated into a practical
# condition, that is, whether a particular indicator lies above or
# below a certain threshold curve.
#
# This module provides these curves for the first thirty levels of the 
# genuine quantum non-Gaussian hierarchy. These can be used to detect
# certain subset of quantum states with stellar rank up to thirty.
#
# The author acknowledges discussions with Michal Matulik, Lukas Lachman,
# Peter Marek, and Jaromir Fiurasek. This project uses octal precision 
# libraries for Fortran developed by David Bailey.

import numpy as np
import scipy.interpolate as si

class InvalidCurveError (ValueError):
    pass

def threshold_curve (rank : int) -> si.CubicSpline:
    '''
    Constructs an interpolated curve separating states of a particular
    level of the quantum non-Gaussian hierarchy. The states above the 
    curve are guaranteed to have the desired stellar rank.

    Example
    -------
    Given a state, compute the two indicators,

        y :=       Rho[ rank + 1:, rank + 1:].trace()
          == 1.0 - Rho[:rank + 1, :rank + 1 ].trace()
        x := Rho[rank, rank]

    and, if y > threshold_curve(rank)(x), the state belongs to the particular
    hierarchy.

    Parameters
    ----------
    rank : int
        Hierarchy rank.

    Returns
    -------
    scipy.interpolate.CubicSpline
        Curve separating the states of the particular rank from the rest.
    '''

    xy = _threshold_curve_select(rank)
    return si.CubicSpline(xy[:, 0], xy[:, 1])

def _threshold_curve_select (rank):
    match rank:
        case 0x03: return _threshold_curve_03
        case 0x04: return _threshold_curve_04
        case 0x05: return _threshold_curve_05

    raise InvalidCurveError('Requested rank must satisfy (3 <= rank <= 5).')

